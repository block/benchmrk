// gen extracts the CWE weakness hierarchy and category memberships from the
// MITRE XML catalog and emits them as a compact Go source file.
//
// Usage:
//
//	go run ./gen -src /path/to/cwec_latest.xml.zip -out ../data_gen.go
//
// We only pull two things from the 16MB XML:
//
//	parent[cwe] = cwe     ChildOf edges under View 1000 (Research Concepts).
//	                      View 1000 is the primary abstraction tree — almost
//	                      every weakness has exactly one parent here. ~970
//	                      edges.
//
//	categories[cwe] = []  Has_Member back-edges from <Category> elements.
//	                      Categories are MITRE's orthogonal groupings (OWASP
//	                      Top 10 slices, SFP clusters, etc). Two CWEs sharing
//	                      a category is a strong "same thing, different level"
//	                      signal the parent tree misses. ~4240 memberships.
//
// Output is ~80KB of Go source — small enough to commit, so nothing
// downstream needs the XML.
package main

import (
	"archive/zip"
	"bytes"
	"encoding/xml"
	"flag"
	"fmt"
	"go/format"
	"io"
	"log"
	"os"
	"sort"
	"strings"
)

// Catalog mirrors just the elements we need. encoding/xml ignores everything
// not mentioned in the struct tags, so the 15.9MB we don't care about is
// skipped during the parse — no need to model it.
type Catalog struct {
	Version    string     `xml:"Version,attr"`
	Weaknesses []Weakness `xml:"Weaknesses>Weakness"`
	Categories []Category `xml:"Categories>Category"`
}

type Weakness struct {
	ID      int       `xml:"ID,attr"`
	Related []Related `xml:"Related_Weaknesses>Related_Weakness"`
}

type Related struct {
	Nature string `xml:"Nature,attr"`
	CWEID  int    `xml:"CWE_ID,attr"`
	ViewID int    `xml:"View_ID,attr"`
}

type Category struct {
	ID      int      `xml:"ID,attr"`
	Members []Member `xml:"Relationships>Has_Member"`
}

type Member struct {
	CWEID int `xml:"CWE_ID,attr"`
}

func main() {
	src := flag.String("src", "", "path to cwec_*.xml or cwec_*.xml.zip")
	out := flag.String("out", "data_gen.go", "output .go file")
	flag.Parse()
	if *src == "" {
		log.Fatal("-src is required")
	}

	raw, err := read(*src)
	if err != nil {
		log.Fatalf("read %s: %v", *src, err)
	}

	var cat Catalog
	if err := xml.Unmarshal(raw, &cat); err != nil {
		log.Fatalf("parse XML: %v", err)
	}

	// View 1000 ChildOf only. A handful of weaknesses list multiple ChildOf
	// under 1000 (Ordinal="Primary" vs not); we take the first, which is the
	// primary by XML order.
	parent := map[int]int{}
	for _, w := range cat.Weaknesses {
		for _, r := range w.Related {
			if r.Nature == "ChildOf" && r.ViewID == 1000 {
				if _, seen := parent[w.ID]; !seen {
					parent[w.ID] = r.CWEID
				}
			}
		}
	}

	// Category memberships, inverted to cwe → categories for the lookup
	// direction the matcher wants (given two CWEs, do their category sets
	// intersect).
	cats := map[int][]int{}
	for _, c := range cat.Categories {
		for _, m := range c.Members {
			cats[m.CWEID] = append(cats[m.CWEID], c.ID)
		}
	}
	for k := range cats {
		sort.Ints(cats[k])
	}

	log.Printf("cwec %s: %d weaknesses, %d parent edges, %d categories, %d cwes-with-categories",
		cat.Version, len(cat.Weaknesses), len(parent), len(cat.Categories),
		len(cats))

	formatted, err := format.Source(emit(cat.Version, parent, cats))
	if err != nil {
		log.Fatalf("gofmt: %v", err)
	}
	if err := os.WriteFile(*out, formatted, 0o644); err != nil {
		log.Fatalf("write %s: %v", *out, err)
	}
	log.Printf("wrote %s (%d bytes)", *out, len(formatted))
}

// read accepts either the raw XML or the MITRE-distributed zip. The zip
// contains exactly one file (cwec_vN.N.N.xml).
func read(path string) ([]byte, error) {
	if strings.HasSuffix(path, ".zip") {
		zr, err := zip.OpenReader(path)
		if err != nil {
			return nil, err
		}
		defer zr.Close()
		for _, f := range zr.File {
			if strings.HasSuffix(f.Name, ".xml") {
				rc, err := f.Open()
				if err != nil {
					return nil, err
				}
				defer rc.Close()
				return io.ReadAll(rc)
			}
		}
		return nil, fmt.Errorf("no .xml in zip")
	}
	return os.ReadFile(path)
}

func emit(version string, parent map[int]int, cats map[int][]int) []byte {
	var b bytes.Buffer
	fmt.Fprintf(&b, "// Code generated by go run ./gen -src cwec_latest.xml.zip. DO NOT EDIT.\n")
	fmt.Fprintf(&b, "// Source: MITRE CWE Catalog %s\n\n", version)
	fmt.Fprintf(&b, "package cwe\n\n")

	// parent: one int→int; a switch is denser than a map literal and avoids
	// map-init overhead (not that it matters at this size, but the output is
	// noticeably smaller).
	keys := sortedKeys(parent)
	fmt.Fprintf(&b, "// parentOf returns the View 1000 parent of a CWE, or 0 for roots and\n")
	fmt.Fprintf(&b, "// unknown IDs. Generated: %d edges.\n", len(parent))
	fmt.Fprintf(&b, "func parentOf(id int) int {\n\tswitch id {\n")
	for _, k := range keys {
		fmt.Fprintf(&b, "\tcase %d:\n\t\treturn %d\n", k, parent[k])
	}
	fmt.Fprintf(&b, "\t}\n\treturn 0\n}\n\n")

	// categories: cwe → sorted category IDs. uint16 is plenty (category IDs
	// top out around 1600) and halves the data.
	keys = sortedKeys(cats)
	fmt.Fprintf(&b, "// categoriesOf returns the category IDs a CWE belongs to, sorted.\n")
	fmt.Fprintf(&b, "// Generated: %d CWEs, %d total memberships.\n", len(cats), membershipCount(cats))
	fmt.Fprintf(&b, "var categoriesOf = map[int][]uint16{\n")
	for _, k := range keys {
		fmt.Fprintf(&b, "\t%d: {", k)
		for i, c := range cats[k] {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(&b, "%d", c)
		}
		fmt.Fprintf(&b, "},\n")
	}
	fmt.Fprintf(&b, "}\n")

	return b.Bytes()
}

func sortedKeys[V any](m map[int]V) []int {
	ks := make([]int, 0, len(m))
	for k := range m {
		ks = append(ks, k)
	}
	sort.Ints(ks)
	return ks
}

func membershipCount(m map[int][]int) int {
	n := 0
	for _, v := range m {
		n += len(v)
	}
	return n
}
